// funcio´n que resuelve el problema
// comentario sobre el coste, O(f(N))
int resolver(int numero) {

	if (numero == 0) return 0;
	else if (numero == 1) return 1;
	else return (resolver(numero - 1) + resolver(numero - 2));

}

